"""
Q7. Área de um Retângulo: Escreva um algoritmo que solicita ao usuário a largura e o comprimento de um retângulo,
depois calcula e exibe a área do retângulo.
A fórmula para calcular a área de um retângulo é área = largura x comprimento.
"""

print("construindo um triangulo");
cm = float(input("digite o comprimento"));
lr = float(input("digite a largura"));
a = lr * cm;

print(f"o triangulo tem {cm}cm e {lr} de largura, sua area é de {round(a,2)}");